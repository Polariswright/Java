    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //
        if (!txtValor.getText().isEmpty() && !txtDesc.getText().isEmpty()) {
            Servico servico = new Servico();
            ServicoDAO dao = new ServicoDAO();
            servico.setDescricao(txtDesc.getText());
            servico.setValor(Float.parseFloat(txtValor.getText()));
            try {
                dao.create(servico);
            } catch (SQLException ex) {
                System.out.println("Erro ao Cadastrar no banco de dados" + ex);
            }
            try {
                readJTable();
            } catch (SQLException ex) {
                System.out.println("Erro ao conectar ao banco" + ex);
            }

            limpar();

        } else {
            JOptionPane.showMessageDialog(null, "Informe a Descrição e o valor do serviço!");
        }

    }//GEN-LAST:event_btnAddActionPerformed
    public void limpar() {
        txtId.setText("");
        txtDesc.setText("");
        txtValor.setText("");
        txtValor.setText("");

        txtDesc.grabFocus();

        btnAdd.setEnabled(true);
        txtValor.setEnabled(true);
        btnSrch.setEnabled(true);

        btnExcd.setEnabled(false);
        btnUpd.setEnabled(false);

        try {
            readJTable();
        } catch (SQLException ex) {
            System.out.println("Erro ao conectar ao banco" + ex);
        }
    }

