    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tableService.getModel();
        tableService.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);

        ServicoDAO servicoDao = new ServicoDAO();

        for (Servico servico : servicoDao.read()) {
            modelo.addRow(new Object[]{
                servico.getId(),
                servico.getDescricao(),
                servico.getValor()

            });
        }

    }

